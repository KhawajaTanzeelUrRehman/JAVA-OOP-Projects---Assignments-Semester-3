/*In this file we are creating a class with name Count and defining two constructors one is which accept the parameter and other has a default value for count 1.*/
public class Count
{
public int count;//Creating an instance variable of the class.

public Count(int c)//Creating a constructor for the class which accept the parameter.
{
count = c;
}

public Count()// Creating a constructor with default value of the count 1.
{
count = 1;
}
}